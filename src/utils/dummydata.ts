type PriorityChartData = { name: string; total: number };
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export const getPriorityChartData = (tasks: any[]): PriorityChartData[] => {
  const priorityCount: Record<string, number> = {};

  for (const task of tasks) {
    const key = task.priority || "unknown";
    priorityCount[key] = (priorityCount[key] || 0) + 1;
  }

  return Object.entries(priorityCount).map(([priority, total]) => ({
    name: priority,
    total: Number(total), // Ensures it's a number
  }));
};

